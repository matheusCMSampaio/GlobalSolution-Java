CREATE TABLE Usuarios (
    id_usuario NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nm_usuario VARCHAR2(255) NOT NULL,
    email_usuario VARCHAR2(255) NOT NULL UNIQUE,
    senha_usuario VARCHAR2(255) NOT NULL,
    tipo_usuario VARCHAR2(50) NOT NULL,
    dt_usuario TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE Dados (
    id_dados NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dt_dados TIMESTAMP NOT NULL,
    tipo_dados VARCHAR2(255) NOT NULL,
    valor_dados NUMBER(10, 2) NOT NULL
);
